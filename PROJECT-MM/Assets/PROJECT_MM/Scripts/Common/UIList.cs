// UI List : 
//  => UI 파일들의 프리팹 이름과 동일한 enum 형으로 정의할 것
//  => Popup : 일반적인 게임에서 팝업으로 Over되어서 나올 수 있는 UI들을 의미함.
//  => Panel : 게임에서 보통 움직이지않고, 딱 고정되어있는 형태의 UI들을 의미함.
public enum UIList
{
    PANEL_START,

    TitleUI,
    IngameUI,
    LoadingUI,


    PANEL_END,
    POPUP_START,


    PausePopupUI,


    POPUP_END,
}